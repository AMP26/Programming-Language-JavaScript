// for loop
console.log("Printing odd numbers using \"for\" loop");
for(let i = 1; i <= 10; i += 2)
  console.log(i);

// while loop
console.log("Printing even numbers using \"while\" loop");
let j = 0;
while(j <= 10) {
  console.log(j);
  j += 2;
}

// do while loop
console.log("Printing 1 to 10 numbers using \"do while\" loop");
let k = 1;
do {
console.log(k);
k++;
} while(k <= 10);